namespace ProfileService.Domain.Enums;

public enum UserStatus
{
    /// <summary>
    /// Аккаунт забанен/аннулирован (конечный статус, доступ закрыт).
    /// </summary>
    BANNED,

    /// <summary>
    /// Активный — все проверки пройдены, полный доступ.
    /// </summary>
    ACTIVE,

    /// <summary>
    /// Email не подтверждён — ограниченный доступ до подтверждения.
    /// </summary>
    EMAIL_NOT_VERIFIED,

    /// <summary>
    /// Ограничен — временные ограничения (KYC, подозрение на мошенничество и т.п.).
    /// </summary>
    LIMITED,

    /// <summary>
    /// Заблокирован для входа (например, после превышения числа неудачных попыток).
    /// </summary>
    LOCKED_OUT,

    /// <summary>
    /// Запрошено удаление — аккаунт заморожен, доступен период восстановления.
    /// </summary>
    PENDING_DELETION,

    /// <summary>
    /// Удалён окончательно (данные удалены или анонимизированы).
    /// </summary>
    DELETED,

    /// <summary>
    /// Гостевой/временный аккаунт с минимальными правами.
    /// </summary>
    GUEST,

    /// <summary>
    /// Сервисный аккаунт (интеграции, боты) с особым набором прав.
    /// </summary>
    SERVICE_ACCOUNT,
}
