namespace AdService.Domain.Enums;

public enum AdStatus
{
    /// <summary>
    /// Черновик — объявление создано продавцом, но ещё не отправлено на модерацию.
    /// </summary>
    DRAFT,

    /// <summary>
    /// Ожидает модерации — объявление отправлено на проверку модератором.
    /// </summary>
    PENDING,

    /// <summary>
    /// Опубликовано — объявление прошло модерацию и доступно пользователям.
    /// </summary>
    PUBLISHED,

    /// <summary>
    /// Отклонено — модератор отказал в публикации.
    /// </summary>
    DENIED,

    /// <summary>
    /// Приостановлено — временно скрыто, но может быть снова опубликовано.
    /// </summary>
    PAUSED,

    /// <summary>
    /// Архивировано — объявление завершено и хранится только для истории.
    /// </summary>
    ARCHIVED,

    /// <summary>
    /// Удалено — объявление полностью удалено из системы.
    /// </summary>
    DELETED,

    /// <summary>
    /// Истёк срок действия — объявление автоматически стало недоступным.
    /// </summary>
    EXPIRED,

    /// <summary>
    /// Завершено продажей — товар продан, объявление закрыто.
    /// </summary>
    SOLD
}